CREATE TABLE user (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    senha VARCHAR(100) NOT NULL
);

CREATE TABLE pokemon (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    height INT,
    weight INT,
    evolves_from_species VARCHAR(100),
    evolves_to_species VARCHAR(100)
);

CREATE TABLE pokemon_types (
    pokemon_id INT REFERENCES pokemon(id) ON DELETE CASCADE,
    type VARCHAR(50),
    PRIMARY KEY (pokemon_id, type)
);

CREATE TABLE pokemon_abilities (
    pokemon_id INT REFERENCES pokemon(id) ON DELETE CASCADE,
    ability VARCHAR(100),
    PRIMARY KEY (pokemon_id, ability)
);

CREATE TABLE pokemon_stats (
    pokemon_id INT PRIMARY KEY REFERENCES pokemon(id) ON DELETE CASCADE,
    hp INT,
    attack INT,
    defense INT,
    speed INT
);

CREATE TABLE Equipes (
    id_equipe INT PRIMARY KEY AUTO_INCREMENT,
    nome_equipe VARCHAR(100) NOT NULL,
    id_usuario INT -- Opcional, se você tiver um sistema de login
);

CREATE TABLE Pokemon_Equipe (
    id_equipe INT NOT NULL,
    id_pokemon INT NOT NULL,
    PRIMARY KEY (id_equipe, id_pokemon),
    FOREIGN KEY (id_equipe) REFERENCES Equipes(id_equipe),
    FOREIGN KEY (id_pokemon) REFERENCES Pokemon(id_pokemon)
);

